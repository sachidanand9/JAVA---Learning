

public class Calculator {
	public static void main(String[] args) {
		add(3,5);
		subtract(5,3);
		multi(3,3);
	}
	
	public static void add(int a, int b) {
	 System.out.println(a + b);
	}
	
	public static void subtract(int a, int b) {
		System.out.println(a - b);		
	}
	
	public static void multi(int a, int b) {
		System.out.println(a*b);
	}

}
